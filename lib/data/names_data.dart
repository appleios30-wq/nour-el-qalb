    try {
      return names.firstWhere((n) => n.name == name);
    } catch (e) {
      return null;
    }
  }

  static List<AllahName> search(String query) {
    return names.where((n) => 
      n.name.contains(query) || 
      n.meaning.contains(query)
    ).toList();
  }
